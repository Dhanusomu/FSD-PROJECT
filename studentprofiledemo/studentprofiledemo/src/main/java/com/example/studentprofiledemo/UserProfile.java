package com.example.studentprofiledemo;

public class UserProfile {
    private int id;
    private String name;
    private String department;

    public int getId(){
        return id;
    }
    public void setId(){
        this.id=id;
    }
    public String getName(){
        return name;
    }
    public void setName(){
        this.name=name;
    }
    public String getDepartment(){
        return department;
    }
    public void setDepartment(){
        this.department = department;
    }
}
