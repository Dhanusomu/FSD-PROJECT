package com.example.coffee;

public class Coffee {
    private String name;
    private String coffee;
    public void setName(String name)
    {
        this.name=name;
    }
    public String getName()
    {
        return name;
    }
    public void setCoffee(String coffee)
    {
        this.coffee=coffee;
    }
    public String getCoffee()
    {
        return coffee;
    }
}
